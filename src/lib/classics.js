/* 결정적 해시 — 날짜·일주별 '오늘의 3구절' 시드 */
function hashStr(s) {
  let h = 2166136261 >>> 0;
  for (let i = 0; i < s.length; i++) { h ^= s.charCodeAt(i); h = Math.imul(h, 16777619) >>> 0; }
  return h >>> 0;
}

/* 일주별 고정 셔플 순열(Fisher–Yates, 결정적) */
function shuffledIndices(n, seed) {
  const arr = [...Array(n).keys()];
  let s = seed || 1;
  for (let i = n - 1; i > 0; i--) {
    s = (Math.imul(s, 1103515245) + 12345) >>> 0;
    const j = s % (i + 1);
    const t = arr[i]; arr[i] = arr[j]; arr[j] = t;
  }
  return arr;
}

/* 오늘의 3구절 — 일주별 순열을 '매일 3칸씩' 진행.
   매일 확실히 바뀌고, 전체 풀을 다 돌기 전엔(약 18일) 같은 구절이 다시 나오지 않는다. */
export function dailyThree(saju) {
  const n = CLASSICS.length;
  if (!n) return [];
  const perm = shuffledIndices(n, hashStr(saju?.ilju || 'cheonmun'));
  const d = new Date();
  const dayNum = Math.floor(new Date(d.getFullYear(), d.getMonth(), d.getDate()).getTime() / 86400000);
  const base = (((dayNum * 3) % n) + n) % n;
  return [0, 1, 2].slice(0, n).map(k => CLASSICS[perm[(base + k) % n]]);
}

/* 즐겨찾기 — 한자(h)를 식별자로 localStorage 보관 */
const FAV_KEY = 'cm_classic_fav';
export function getClassicFavs() {
  try { return JSON.parse(localStorage.getItem(FAV_KEY) || '[]'); } catch { return []; }
}
export function isClassicFav(h) { return getClassicFavs().includes(h); }
export function toggleClassicFav(h) {
  const f = getClassicFavs();
  const i = f.indexOf(h);
  if (i >= 0) f.splice(i, 1); else f.unshift(h);
  try { localStorage.setItem(FAV_KEY, JSON.stringify(f.slice(0, 200))); } catch { /* 용량 초과 무시 */ }
  return f.includes(h);
}
